declare(strict_types=1);

namespace Classi;

class Utility{

	public static function getStringBetween(string $string, string $start, string $end) : string{
		$s = strpos($string, $start) + strlen($start);
		$e = strpos($string, $end, $s + 1);
		return substr($string, $s, $e - $s);
	}
}