declare(strict_types=1);

namespace Classi;

abstract class Content{

	/** @var Client */
	protected $client;

	/** @var string */
	protected $url;

	public function __construct(Client $client, string $url){
		$this->client = $client;
		$this->url = $url;
	}

	abstract public function getType() : string;

	abstract public function doHomework() : void;
}